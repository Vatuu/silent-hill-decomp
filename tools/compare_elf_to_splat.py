#!/usr/bin/env python3

# Usage:
#   tools/compare_elf_to_splat.py [--show-stale] <elf path> <splat sym.txt path>
# eg:
#   tools/compare_elf_to_splat.py build/USA/out/1ST/BODYPROG.BIN.elf configs/USA/sym.bodyprog.txt
#
# --show-stale can be used to show syms that are in splat sym.txt but not in elf (lots of false positives / psyq stuff)

import sys
import argparse
import re
from elftools.elf.elffile import ELFFile

def parse_sym_addrs(content):
    func_dict = {}

    lines = content.strip().splitlines()
    for line in lines:
        line = line.strip()
        if not line or line.startswith('//'):
            continue

        parts = line.split('=')
        if len(parts) != 2:
            continue

        func_name = parts[0].strip()
        value_part = parts[1].split('//')[0].strip()

        if value_part.startswith("0x"):
            try:
                if value_part.endswith(";"):
                    value_part = value_part[:-1]
                value = int(value_part, 16)
                func_dict[func_name] = value
            except ValueError:
                continue
        else:
            continue

    return func_dict

def read_file(path):
    try:
        with open(path, 'r') as file:
            return file.read()
    except Exception as e:
        print(f"Error reading {path}: {e}")
        return None

def parse_elf(path):
    symbols = {}  # addr -> name
    base_addr = 0
    end_addr = None

    IGNORED_SYMBOLS = {"__gnu_compiled_c", "gcc2_compiled."}
    IGNORED_SUFFIXES = ("_ROM_START", "_ROM_END", "_RODATA_START", "_RODATA_END", "_BSS_START", "_BSS_END", "_TEXT_START", "_TEXT_END", "_DATA_START", "_DATA_END", "_SDATA_START", "_SDATA_END", "_VRAM", "_VRAM_END", "_DATA_SIZE", "_bin_end")

    with open(path, 'rb') as f:
        elffile = ELFFile(f)

        # Base address from first PT_LOAD segment
        for segment in elffile.iter_segments():
            if segment['p_type'] == 'PT_LOAD':
                base_addr = segment['p_vaddr']
                break

        # Fetch symbols
        vram_ends = []
        for section in elffile.iter_sections():
            if not hasattr(section, 'iter_symbols'):
                continue
            for sym in section.iter_symbols():
                addr = sym['st_value']
                name = sym.name
                if addr != 0 and name:
                    # Use VRAM_END to tell us where overlay memory range ends
                    if name.endswith('_VRAM_END'):
                        vram_ends.append(addr)

                    # Ignore symbols that could overwrite actual symbol name at that addr.
                    if name in IGNORED_SYMBOLS or any(name.endswith(s) for s in IGNORED_SUFFIXES):
                        continue
                    
                    # ELF symbol collision check
                    #if addr in symbols and symbols[addr] != name:
                    #    print(f"  collision at 0x{addr:08X}: {symbols[addr]} -> {name}")

                    symbols[addr] = name

        if vram_ends:
            end_addr = min(vram_ends)

    return symbols, base_addr, end_addr

def main():
    parser = argparse.ArgumentParser(description="Compare ELF symbols against splat symbol file")
    parser.add_argument("elf_path", help="Path to the ELF file")
    parser.add_argument("sym_path", help="Path to the splat symbol file")
    parser.add_argument("--show-stale", action="store_true", help="Show symbols in splat file that are not in the ELF")
    args = parser.parse_args()

    content = read_file(args.sym_path)
    if content is None:
        sys.exit(1)

    splat_symbols = parse_sym_addrs(content)
    elf_symbols, elf_base_addr, elf_end_addr = parse_elf(args.elf_path)

    # Strip compiler-generated dot suffixes from splat symbols
    splat_normalized = {}
    for name, addr in splat_symbols.items():
        base = re.sub(r'\.\d+$', '', name)
        splat_normalized[base] = addr

    # Build name -> addr from ELF
    elf_by_name = {}
    for addr, name in elf_symbols.items():
        # Skip symbols outside of the overlay we're checking
        if elf_base_addr >= addr or (elf_end_addr and addr >= elf_end_addr):
            continue

        elf_by_name[name] = addr

    missing = []
    mismatched = []

    for name, addr in elf_by_name.items():
        # Skip autogenerated symbol names
        if name.startswith("func_") or name.startswith("D_"):
            continue
        # Prefer exact name match but fall back to normalized (dot-suffix stripped) match.
        # This avoids false mismatches when multiple statics share the same base name
        if name in splat_symbols:
            if splat_symbols[name] != addr:
                mismatched.append((name, addr, splat_symbols[name]))
        else:
            base = re.sub(r'\.\d+$', '', name)
            if base not in splat_normalized:
                missing.append((name, addr))
            elif splat_normalized[base] != addr:
                mismatched.append((name, addr, splat_normalized[base]))

    print(f"=== Missing from {args.sym_path} ({len(missing)}) ===")
    for name, addr in sorted(missing, key=lambda x: x[1]):
        print(f"  {name} = 0x{addr:08X};")

    print(f"\n=== Address mismatch ({len(mismatched)}) ===")
    for name, elf_addr, splat_addr in sorted(mismatched, key=lambda x: x[1]):
        print(f"  {name}: elf=0x{elf_addr:08X} splat=0x{splat_addr:08X}")

    if args.show_stale:
        # Build ELF symbol list with dot suffixes removed
        elf_normalized = {re.sub(r'\.\d+$', '', name) for name in elf_by_name}
        
        # Stale: in splat but not in ELF
        stale = []
        for name, addr in splat_symbols.items():
            if name in elf_by_name:
                continue
            base = re.sub(r'\.\d+$', '', name)
            if base not in elf_normalized:
                stale.append((name, addr))

        print(f"\n=== Stale in {args.sym_path} (not in ELF) ({len(stale)}) ===")
        print(f"=== (may be false positives if symbols overlap) ===")
        for name, addr in sorted(stale, key=lambda x: x[1]):
            print(f"  {name} = 0x{addr:08X};")

    if missing or mismatched or stale:
        sys.exit(1)

if __name__ == "__main__":
    main()
